import Foundation

extension UIColor {
    
    class func destructiveActionSheetCell() -> UIColor {
        return UIColor(red: 249.0/255.0, green: 95.0/255.0, blue: 98.0/255.0, alpha: 1.0)
    }
    
    class func defaultActionSheetCell() -> UIColor {
        return UIColor(red: 0.0, green: 166.0/255.0, blue: 255.0/255.0, alpha: 1.0)
    }
    
}
